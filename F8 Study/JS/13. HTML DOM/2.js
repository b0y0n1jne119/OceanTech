// HTML DOM vs DOM API

/*
    
    DOM API (Document Object Model Application Programming Interface) cung cấp một tập hợp các phương thức và thuộc tính trong JavaScript, để truy cập và tương tác với các phần tử HTML, CSS và JavaScript trong trang web được hiển thị trên trình duyệt.

    Nhờ vào DOM API, JavaScript có thể truy cập và tương tác với các phần tử trên trang web, thêm hoặc xóa các phần tử, thay đổi nội dung của chúng, tạo ra các sự kiện và nhiều hoạt động khác. Ví dụ, các phương thức và thuộc tính như document.getElementById(), element.innerHTML, element.style, element.addEventListener(),... là những thứ được cung cấp bởi DOM API để cho JavaScript có thể tương tác với trang web.

    nhu vay document.getElementById() là một phương thức được cung cấp bởi DOM API trong JavaScript để truy cập và lấy thông tin về một phần tử trong trang web dựa trên giá trị của thuộc tính id của phần tử đó.

    Phương thức getElementById() là một ví dụ của những phương thức và thuộc tính được cung cấp bởi DOM API để cho JavaScript có thể tương tác với HTML và CSS của trang web. Nó cho phép lập trình viên lấy thông tin về một phần tử cụ thể trong trang web và thực hiện các thao tác trên phần tử đó, chẳng hạn như thay đổi nội dung, kiểu dáng hay thêm/xóa phần tử.

    Vì vậy, document.getElementById() là một ví dụ rõ ràng cho việc JavaScript sử dụng DOM API để tương tác với các phần tử trong trang web. còn document là 1 đối tượng trong js

    1. Element
    2. Attribute
    3. Text

    -----------------------------

    Javascript: chạy ở các trình duyệt or Server (NodeJS)

    Browers: HTML -> DOM -> WEB API ( Application programming interface: 1 con đường mở ra vô vàn các con đường khác)

*/